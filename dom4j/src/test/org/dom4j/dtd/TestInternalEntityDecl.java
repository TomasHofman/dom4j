package org.dom4j.dtd;

import junit.framework.*;
import junit.textui.TestRunner;

/** Tests the {@link InternalEntityDecl} functionality.  Tests each of
  * the property access methods and the serialization mechanisms.
  * Correct parsing is tested by {@link TestDocType}.<p>
  *
  * @todo The dom4j documentation needs to describe what
  * representation SHOULD be generated by {@link
  * InternalEntityDecl#toString()}.
  *
  * @author Bryan Thompson
  * @author Maarten Coene
  * @version $Revision: 1.4 $
  */

public class TestInternalEntityDecl extends TestCase {

    
    public static void main( String[] args ) {
        TestRunner.run( suite() );
    }
    
    public static Test suite() {
        return new TestSuite( TestInternalEntityDecl.class );
    }
    
    public TestInternalEntityDecl(String name) {
        super(name);
    }

    // Test case(s)
    //-------------------------------------------------------------------------                    
    public void testToString() {
        InternalEntityDecl decl1 = new InternalEntityDecl("name", "value");
        InternalEntityDecl decl2 = new InternalEntityDecl("%name", "value");
        
        assertEquals("<!ENTITY name \"value\">", decl1.toString());
        assertEquals("<!ENTITY % name \"value\">", decl2.toString());
    }

    /**
     * Tests parameter entity declaration, such as
     *
     * <pre>
     * <!ENTITY % boolean "( true | false )">
     * </pre>
     * Note: There is a required whitespace between the "%" and the
     * name of the entity.  This whitespace is required in the
     * declaration and is not allowed when writing a reference to the
     * entity, e.g., "%boolean;" is a legal reference but not "%
     * boolean;".<p>
     *
     * Note: The "%" is part of the parameter entity name as reported
     * by the SAX API.  See
     *
     * <a href="http://www.saxproject.org/apidoc/org/xml/sax/ext/LexicalHandler.html#startEntity(java.lang.String)">
     * LexicalHandler</a>, which states:
     *
     * <pre>
     * General entities are reported with their regular names,
     * parameter entities have '%' prepended to their names, and the
     * external DTD subset has the pseudo-entity name "[dtd]".
     * </pre>
     */
    public void test_parameter_entity()
    {
	
	String expectedName = "%boolean";

	String expectedValue = "( true | false )";

	String expectedText = "<!ENTITY % boolean \"( true | false )\">";

	InternalEntityDecl actual = new InternalEntityDecl
	    ( expectedName,
	      expectedValue
	      );

	assertEquals
	    ( "name is correct",
	      expectedName,
	      actual.getName()
	      );

	assertEquals
	    ( "value is correct",
	      expectedValue,
	      actual.getValue()
	      );

	assertEquals
	    ( "toString() is correct",
	      expectedText,
	      actual.toString()
	      );

    }

    /**
     * Tests general entity declaration, such as:
     *
     * <pre>
     * <!ENTITY foo "bar">
     * </pre>
     */

    public void test_general_entity()
    {

	String expectedName = "foo";

	String expectedValue = "bar";

	String expectedText = "<!ENTITY foo \"bar\">";

	InternalEntityDecl actual = new InternalEntityDecl
	    ( expectedName,
	      expectedValue
	      );

	assertEquals
	    ( "name is correct",
	      expectedName,
	      actual.getName()
	      );

	assertEquals
	    ( "value is correct",
	      expectedValue,
	      actual.getValue()
	      );

	assertEquals
	    ( "toString() is correct",
	      expectedText,
	      actual.toString()
	      );

    }

    // Implementation methods
    //-------------------------------------------------------------------------                    


}



